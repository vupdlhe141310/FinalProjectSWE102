    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int ma_san_pham = Integer.parseInt(request.getParameter("ma_san_pham"));
        String li_do = request.getParameter("ReportContent");
        ReportedDAO rd = new ReportedDAO();
        try {
            rd.insertReport(ma_san_pham, li_do);
        } catch (SQLException ex) {
            Logger.getLogger(ReportedProServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
        request.getRequestDispatcher("detail.jsp?ma_san_pham="+ma_san_pham).forward(request, response);
        
                
    }

